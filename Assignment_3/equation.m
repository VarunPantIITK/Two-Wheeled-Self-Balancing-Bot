function dy_dx = equation(t,y,A)
dy_dx(1,1) = (A(1,1))*y(1)+(A(1,2))*y(2)+(A(1,3))*y(3)+(A(1,4))*y(4);
dy_dx(2,1) = (A(2,1))*y(1)+(A(2,2))*y(2)+(A(2,3))*y(3)+(A(2,4))*y(4);
dy_dx(3,1) = (A(3,1))*y(1)+(A(3,2))*y(2)+(A(3,3))*y(3)+(A(3,4))*y(4);
dy_dx(4,1) = (A(4,1))*y(1)+(A(4,2))*y(2)+(A(4,3))*y(3)+(A(4,4))*y(4);
